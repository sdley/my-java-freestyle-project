void main() {

    // Operations Order [PEMDAS / BODMAS]
    // PEMDAS: Parentheses, Exponents, Multiplication and Division (from left to right),
    //                                      Addition and Subtraction (from left to right)
    // BODMAS: Brackets, Orders (i.e. powers and square roots, etc.), Division and Multiplication (from left to right),
    //                                                                    Addition and Subtraction (from left to right)

    double result = 3 + 2 * (4 - 1) / 3.0;

    System.out.println("Result: " + result); // Result: 5.0
}